<p>Death/Reincarnation</p>

<p>A Chao can do 2 things when it reaches an old age. It can 1, die, or 2, reincarnate. A Chao that has been treated badly for most of its life will go into a GRAY TEAR DROP SHAPED SHELL and die. A Chao that has been treated generally nice will go into a PINK TEAR DROP SHAPED SHELL and will come out as an egg. This egg contains your original Chao. All of the Chao’s original stats will be divided by 10 and it will be baby again.
</p>
<p>How a Chao evolves</p>

<p>As you should know, a Chao will evolve into a certain type depending on who raises it and how they do it. A dark character treating a Chao nicely will make a dark Chao, and vise-versa with how a hero character treats the Chao. There are six different paths a Chao can evolve.
Hero 1: A Chao can become a hero Chao by being treated nicely with a hero character.<br>
Hero 2: A Chao can become a hero Chao by being treated badly with a dark character. (NOT RECOMMENDED)<br>
Dark 1: A Chao can become a dark Chao by being treated nicely with a dark character.<br>
Dark 2: A Chao can become a dark Chao by being treated badly with a hero character (NOT RECOMMENDED)<br>
Normal 1: A Chao can become a normal Chao by being treated nicely by both a hero and dark character equally<br>
Normal 2:A Chao can become a normal Chao by just being left alone and not touched by either a hero or dark character.<br>
</p>
<p>What a Chao can evolve into</p>

<p>A Chao can evolve into 10 normal types of Chao. Each type has either a light or dark attribute.
Flying: These excel in flying stats. These are made with purple Chaos drives and flying animals.<br>
Running: These excel in running stats. These are made with green Chaos drives and running animals.<br>
Power: These excel in power stats. These are made with red Chaos drives and power animals<br>
Swimming: These excel in swimming stats. These are made with yellow Chaos drives and swimming animals.<br>
Normal: These Chao excel in stamina stats. They are made with the garden fruit, these types of Chao can also be made by raising a Chao in the tiny Chao garden, found in both sonic advanced 1 and 2.<br>
</p>
<p>Chaos Chao</p>

<p>After a Chao reincarnates twice, you can evolve it into a Chaos Chao. On the second reincarnation, you CANNOT feed it ANY Chaos drives. There are 3 types of Chaos Chao. Neutral, hero, and dark. They are made normally, but no Chaos drives. A rumor going around is that you must feed it only Chao, hero, or dark fruit. This rumor is misleading. In the newer version of sonic adventure, Sonic adventure DX directors cut, you have to use fruit to have a Chao evolve into a hero or dark Chaos Chao. 
</p>

