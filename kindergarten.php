<p>-Heath Center-</p>

<p>The Health center is located in the Chao Kindergarden just right of the Principal. The Health Center is by far one of the more important sections. The first option it gives you is to check your chao's health. If your chao is sick he will give you medicine. 
</p>
<p>The next option is to check your chao's stats. It will tell you if he is neutral, hero, or dark. It will tell what stage of his life he is in. It will also tell you his stats in each catagory. You will see his swim, fly, run, power, and stamina grades (note: grades vary from E being the worst, D, C, B, A, and S being the best) 
</p>
<p>The Next thing will tell you a little more about the chao itself than any of the above sections. It will tell you his Personality, Favorite Fruit, Age, how many times transformed, and it's current medical condition. 
</p>
<p>The Last thing the Health center can do for you is tell show you how many race medals your chao has won, and it's win to lose ratio in chao karate. To learn more about chao Karate and Chao race visit Chao Stadium 
