<p>The default faces of the chao are gotten randomly. Here is an easy trick to get the face you want, get an egg, save, don't hatch the egg, leave chao garden. Re-enter chao garden and hatch the egg, if you don't like the face then reset the game. (Soft reset is recommended.) Keep repeating these steps until you are satisfied with your chao's face. There are default faces which are "Dizzy/Grumbly", a zig-zag line for a mouth. "Regular Eyes" which are eyes that are wide open for a chao (normal chao eyes). "Sleepy Eyes" which are eyes that are only half open giving the look that the chao is tired. "Evil Eyes" which are eyes that have a straight line for their eyelid going towards the center giving the chao an evil/maniacal look. "Happy Mouth" is a mouth in a v-shape that indicates the chao is happy. "No Mouth" means the chao has no mouth. "Sad Mouth" means the chao has a mouth in an upside-down v shape making it look like the chao is sad. "Evil Mouth" means the chao has a big grin with pointy teeth all the time giving the chao a kind of evil/maniacal look. Note that one there are one set of special eyes and one special mouth for each chao so you can get a chao with "Sleepy Eyes" and a "Grumbly/Dizzy/Tired Mouth" or a chao with "Evil Eyes" and an "Evil Mouth". There are many combinations.
